<script type="text/javascript">
  import BScroll from 'better-scroll'
  import {saveToLocal, loadFromLocal} from '@/common/js/localStorage'
  import Star from '@/components/star/star'
  import Split from '@/components/split/split'

  export default {
    props: {
      seller: Object
    },
    data () {
      return {
        // 闭包, 立即执行, 获取localStorage中的值
        favorite: (() => {
          return loadFromLocal(this.seller.id, 'favorite', false)
        })()
      }
    },
    created () {
      this.classMap = ['decrease', 'discount', 'special', 'invoice', 'guarantee']
    },
    watch: {
      /**
       * 没有封装成独立的scroll组件时, 就要这么写(仿照封装的scroll):
       * 1. 在mounted时, 初始化scroll
       * 2. 还要监听请求的数据的变化, 执行refresh()来更新scroll
       * 这样才能实现更新数据时, 动态执行scroll
       * */
      seller () {
        this.$nextTick(() => {
          this._initScroll()
          this._initPics()
        })
      }
    },
    mounted () {
      this.$nextTick(() => {
        this._initScroll()
        this._initPics()
      })
    },
    computed: {
      favoriteText () {
        return this.favorite ? '已收藏' : '收藏'
      }
    },
    methods: {
      toggleLike () {
        this.favorite = !this.favorite
        saveToLocal(this.seller.id, 'favorite', this.favorite)
      },
      _initScroll () {
        if (!this.scroll) {
          this.scroll = new BScroll(this.$refs.sellerWrapper, {
            click: true
          })
        } else {
          this.scroll.refresh()
        }
      },
      _initPics () {
        // 图片滚动
        if (this.seller.pics) {
          let picWidth = 120
          let marginR = 6
          let width = (picWidth + marginR) * this.seller.pics.length - marginR
          this.$refs.picList.style.width = width + 'px'
          if (!this.picScroll) {
            this.picScroll = new BScroll(this.$refs.picWrapper, {
              scrollX: true,
              // 忽略纵向滚动
              eventPassthrough: 'vertical'
            })
          } else {
            this.picScroll.refresh()
          }
        }
      }
    },
    components: {
      Star,
      Split
    }
  }
</script>

